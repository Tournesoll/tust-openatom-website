#!/bin/bash

echo "========================================"
echo "  关闭占用 8888 端口的进程"
echo "========================================"
echo ""

echo "正在查找占用 8888 端口的进程..."
lsof -ti:8888

if [ $? -eq 0 ]; then
    echo ""
    echo "正在关闭占用端口的进程..."
    lsof -ti:8888 | xargs kill -9
    echo "完成！端口 8888 已释放。"
else
    echo "未找到占用 8888 端口的进程。"
fi

echo ""

